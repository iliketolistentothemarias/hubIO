import { Resource, ResourceCategory } from '@/lib/types'
import { parseAddress, emailFromWebsite, normWebsite } from './resource-helpers'

type Opts = {
  rating?: number
  reviewCount?: number
  hours?: string
  services?: string[]
  capacity?: number
  languages?: string[]
  featured?: boolean
  accessibility?: string[]
}

function e(
  id: string,
  name: string,
  category: ResourceCategory,
  description: string,
  address: string,
  coords: { lat: number; lng: number },
  phone: string,
  website: string,
  tags: string[],
  opts: Opts = {}
): Resource {
  const w = normWebsite(website)
  return {
    id,
    name,
    category,
    description,
    address,
    location: parseAddress(address, coords),
    phone,
    email: emailFromWebsite(w),
    website: w,
    tags,
    featured: opts.featured ?? false,
    verified: true,
    coordinates: coords,
    rating: opts.rating,
    reviewCount: opts.reviewCount,
    hours: opts.hours,
    services: opts.services,
    capacity: opts.capacity,
    languages: opts.languages,
    accessibility: opts.accessibility ?? ['Wheelchair Accessible', 'Parking Available'],
    createdAt: new Date(),
    updatedAt: new Date(),
  }
}

